    private int getNextIdForCourse(String courseCodePrefix) {
    int max = 0;
    String query = "SELECT student_id FROM student WHERE student_id LIKE ?";

    try (Connection conn = DatabaseManager.getConnection();
         java.sql.PreparedStatement pst = conn.prepareStatement(query)) {

        pst.setString(1, courseCodePrefix + "-%");
        try (java.sql.ResultSet rs = pst.executeQuery()) {
            while (rs.next()) {
                String sid = rs.getString("student_id");
                // Extract number part
                String[] parts = sid.split("-");
                if (parts.length == 2) {
                    try {
                        int num = Integer.parseInt(parts[1]);
                        if (num > max) max = num;
                    } catch (NumberFormatException ignored) {}
                }
            }
        }

    } catch (Exception e) {
    }

    return max + 1;
}

    private void generateStudentId() {
    String name = txtName.getText().trim();
    String course = (String) cmbCourse.getSelectedItem();

    if (!name.isEmpty() && courseCodes.containsKey(course)) {
        String code = courseCodes.get(course);
        int nextNumber = getNextIdForCourse(code);
        String studentId = String.format("%s-%03d", code, nextNumber);
        txtStudentId.setText(studentId);
    } else {
        txtStudentId.setText("");
    }
    txtStudentId.setEditable(false);
  
}
