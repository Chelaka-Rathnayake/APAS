    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
           String updateId = txtUpdateId.getText().trim();
    String newName = txtName.getText().trim();
    String newCourse = (String) cmbCourse.getSelectedItem();

    if (updateId.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter the Student ID to update.");
        return;
    }

    StringBuilder sql = new StringBuilder("UPDATE student SET ");
    boolean updateName = !newName.isEmpty();
    boolean updateCourse = cmbCourse.getSelectedIndex() != -1;

    if (!updateName && !updateCourse) {
        javax.swing.JOptionPane.showMessageDialog(this, "Enter a new Name or select a Course to update.");
        return;
    }

    if (updateName) sql.append("name = ?");
    if (updateCourse) {
        if (updateName) sql.append(", ");
        sql.append("course = ?");
    }
    sql.append(" WHERE student_id = ?");

    try (Connection conn = DatabaseManager.getConnection();
         java.sql.PreparedStatement pst = conn.prepareStatement(sql.toString())) {

        int index = 1;
        if (updateName) pst.setString(index++, newName);
        if (updateCourse) pst.setString(index++, newCourse);
        pst.setString(index, updateId);

        int rows = pst.executeUpdate();
        if (rows > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Student updated.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No student found with that ID.");
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Update failed: " + e.getMessage());
    }

    txtName.setText("");
    cmbCourse.setSelectedIndex(-1);
    txtUpdateId.setText("");
    }//GEN-LAST:event_btnupdateActionPerformed

