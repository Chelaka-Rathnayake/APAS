    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
                                            
    String name = txtName.getText().trim();
    String course = (String) cmbCourse.getSelectedItem();
    String studentId = txtStudentId.getText();

    if (name.isEmpty() || course == null || studentId.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a name and select a course.");
        return;
    }

    try (Connection conn = DatabaseManager.getConnection()) {
        String sql = "INSERT INTO student (student_id, name, course) VALUES (?, ?, ?)";
        try (java.sql.PreparedStatement pst = conn.prepareStatement(sql)) {
            pst.setString(1, studentId);
            pst.setString(2, name);
            pst.setString(3, course);
            pst.executeUpdate();
        }

        javax.swing.JOptionPane.showMessageDialog(this, "Student added:\n" + studentId + " - " + name + " (" + course + ")");
    } catch (SQLException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error saving student: " + ex.getMessage());
    }

    // Clear form and increment counter
    txtName.setText("");
    cmbCourse.setSelectedIndex(-1);
    txtStudentId.setText("");
    studentCounter++;


    }//GEN-LAST:event_btnaddActionPerformed
    
            
